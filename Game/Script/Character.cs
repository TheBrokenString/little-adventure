    //¼ÆËãplayerÒÆ¶¯
    private void CalculatePlayerMovement()
    {
        if(_playerInput.MouseButtonDown && _cc.isGrounded)
        {
            SwitchStateTo(CharacterState.Attacking); //Âú×ãÌõ¼þ½ÇÉ«½øÈë¹¥»÷×´Ì¬
            return;
        }
        else if(_playerInput.SpaceKeyDown&&_cc.isGrounded)
        {
            SwitchStateTo(CharacterState.Slide);//°´ÏÂ¿Õ¸ñ·­¹ö
            return;
        }

        _movementvelocity.Set(_playerInput.HorizontalInput, 0f, _playerInput.verticalInput); //ï¿½ï¿½ï¿½Ã´ï¿½Ö±ï¿½ï¿½ï¿½ï¿½Îª0ï¿½ï¿½Ö¹ï¿½ï¿½É«ï¿½ï¿½Yï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½Æ¶ï¿½
        _movementvelocity.Normalize(); //ï¿½æ·¶ï¿½ï¿½ï¿½Ù¶È£ï¿½ï¿½ï¿½È»ï¿½ï¿½Äªï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿??
        _movementvelocity = Quaternion.Euler(0, -45, 0) * _movementvelocity; //Æ¥ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½?ï¿½Ù¶ï¿½
        _animator.SetFloat("Speed", _movementvelocity.magnitude);
        _movementvelocity *= MoveSpeed * MoveSpeed * Time.deltaTime; //È·ï¿½ï¿½ï¿½ï¿½Ã¿Ö¡ï¿½Ïµï¿½ï¿½Æ¶ï¿½ï¿½Ù¶ï¿½ï¿½Çºã¶¨ï¿½ï¿½
        
        if (_movementvelocity != Vector3.zero)
        {
            transform.rotation = Quaternion.LookRotation(_movementvelocity);//¹¥»÷³¯ÏòÍæ¼Ò
        }
        _animator.SetBool("AirBorne", !_cc.isGrounded);

        //RotateToCursor();
    }

    private void CalculateEnemyMovement()
    {
        
        if (Vector3.Distance(TargetPlayer.position, transform.position) >= _navMesAgent.stoppingDistance)
        {
            _navMesAgent.SetDestination(TargetPlayer.position);
            _animator.SetFloat("Speed", 0.2f);
        }
        else
        {
            _navMesAgent.SetDestination(transform.position);
            _animator.SetFloat("Speed",0f);

            SwitchStateTo(CharacterState.Attacking); //µÐÈË×·»÷player£¬µ±¾àÀë×ã¹»¾Í¿ÉÒÔ¹¥»÷player
        }
    }

    //¹Ì¶¨¸üÐÂ
    private void FixedUpdate()
    {
        switch (CurrentState)
        {
            case CharacterState.Normal:
                if (IsPlayer && GameObject.FindGameObjectWithTag("Player"))
                {
                    CalculatePlayerMovement();
                }
                else
                {
                    CalculateEnemyMovement();
                }
                break
                    ;
            case CharacterState.Attacking:
                if (IsPlayer)
                {
                    
                    
                    if(Time.time<attackStartTime+AttackSlideDuration)
                    {
                        float timePassed = Time.time - attackStartTime;
                        float lerpTime = timePassed / AttackSlideDuration;
                        _movementvelocity = Vector3.Lerp(transform.forward * AttackSlideSpeed, Vector3.zero, lerpTime);//combo¹¥»÷Ê±ÏòÇ°»¬ÐÐÒ»Ð¡¶Î¾àÀë
                    }
                    if(_playerInput.MouseButtonDown&&_cc.isGrounded)
                    {
                        string currentClipName = _animator.GetCurrentAnimatorClipInfo(0)[0].clip.name;//ï¿½ï¿½È¡ï¿½ï¿½Ç°ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½ï¿½
                        attackAnimationDuration = _animator.GetCurrentAnimatorStateInfo(0).normalizedTime;

                        if(currentClipName != "LittleAdventurerAndie_ATTACK_03"&&attackAnimationDuration>0.5f &&attackAnimationDuration<0.7f)
                        {
                            _playerInput.MouseButtonDown = false;
                            SwitchStateTo(CharacterState.Attacking);

                            //CalculatePlayerMovement();
                        }
                    }
                }
                break;

            case CharacterState.Dead:
                return;
            case CharacterState.BeingHit:
                break;
            case CharacterState.Slide:
                _movementvelocity = transform.forward * SlideSpeed * Time.deltaTime;
                break;
            case CharacterState.Spawn:
                currentSpwanTime -= Time.deltaTime;
                if(currentSpwanTime<=0)
                {
                    SwitchStateTo(CharacterState.Normal);
                }
                break;
        }

        if (impactOnCharacter.magnitude > 0.2f)
        {
            _movementvelocity = impactOnCharacter * Time.deltaTime; //»÷ÍË
        }
        impactOnCharacter = Vector3.Lerp(impactOnCharacter, Vector3.zero, Time.deltaTime * 5);//

        if (IsPlayer)
        {
            if (_cc.isGrounded == false)//

                _vertivalvelocity = Gravity;//·ÀÖ¹playerÒòÎªÖØÁ¦·ÉÆðÀ´

            else
                _vertivalvelocity = Gravity * 0.3f;
            _movementvelocity += _vertivalvelocity * Vector3.up * Time.deltaTime;


            _cc.Move(_movementvelocity);
            _movementvelocity = Vector3.zero;
        }
        else
        {
            if(CurrentState!=CharacterState.Normal)
            {
                _cc.Move(_movementvelocity);
                _movementvelocity = Vector3.zero;
            }
        }
        
    }

    //ÇÐ»»×´Ì¬
    public void SwitchStateTo(CharacterState newState )
    {

        if (IsPlayer)
        {
            //Çå³þ»º´æ
            _playerInput.ClearCache();


        }

        //ÍË³ö×´Ì¬
        switch (CurrentState)
        {
            case CharacterState.Normal:
                break;
            case CharacterState.Attacking:

                if (_damageCaster !=null)
                {
                    _damageCaster.DisableDamageCaster();
                }
                if (IsPlayer)
                {
                    GetComponent<PlayerVFXManager>().StopBlade();
                }
                break;
            case CharacterState.Dead:
                return;
            case CharacterState.BeingHit:
                break;
            case CharacterState.Slide:
                break;
            case CharacterState.Spawn:
                IsInvincible = false;
                break;

                
        }

        //½øÈë×´Ì¬
        switch (newState)
        {
            case CharacterState.Normal:
                break;
            case CharacterState.Attacking:
                if(!IsPlayer)
                {
                    Quaternion newRotation = Quaternion.LookRotation(TargetPlayer.position - transform.position);//³¯Ç°·½¹¥»÷
                    transform.rotation = newRotation; 

                }
                _animator.SetTrigger("Attack");

                if (IsPlayer)
                {
                    attackStartTime = Time.time;
                    RotateToCursor();//°´Êó±ê³¯Ïò½øÐÐ¹¥»÷
                }
                break;
            case CharacterState.Dead:
                _cc.enabled = false;
                _animator.SetTrigger("Dead");
                StartCoroutine(MaterialDissolve());

                if (!IsPlayer)
                {
                    SkinnedMeshRenderer mesh = GetComponentInChildren<SkinnedMeshRenderer>();
                    mesh.gameObject.layer = 0;//½â¾öµÐÈËËÀÍöÒÅÁôµÄÍø¸ñ
                }

                break;
            case CharacterState.BeingHit:
                _animator.SetTrigger("BeingHit");

                if(IsPlayer)
                {
                    IsInvincible = true;
                    StartCoroutine(DelayCancelInvincible());
                }
                break;
            case CharacterState.Slide:
                _animator.SetTrigger("Slide");
                break;
            case CharacterState.Spawn:
                IsInvincible = true;
                currentSpwanTime = SpawnDuration;
                StartCoroutine(MaterialAppear());
                break;
        }

        CurrentState = newState;

        if(IsPlayer)
        {
            Debug.Log("Playerµ±Ç°×´Ì¬" + CurrentState);
        }
        else
            Debug.Log("Enemyµ±Ç°×´Ì¬" + CurrentState);
    }



